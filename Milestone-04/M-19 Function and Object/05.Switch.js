// Alternative and Faster Version fo If Else

const color = 'Blue';

switch (color) {
    case 'White':
        console.log('White')
        break;
    case 'Orange':
        console.log('Orange')
        break;
    case 'Blue':
        console.log('Blue')
        break;    
    default:
       console.log('Nothing')
}


// Days using Switch
const days = 'Wednesday';

switch (days) {
    case 'Saturday':
        console.log('Today is Saturday')
        break;
   
    case 'Sunday':
        console.log('Today is Sunday')
        break;
   
    case 'Monday':
        console.log('Today is Monday')
        break;
   
    case 'Tuesday':
        console.log('Today is Tuesday')
        break;
   
    case 'Wednesday':
        console.log('Today is Wednesday')
        break;
   
    case 'Thursday':
        console.log('Today is Thursday')
        break;
   
    default:
        console.log('Today is Friday')
}